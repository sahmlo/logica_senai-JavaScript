// Operadores

let nome1 = 'Marta'
let nome2 = 'Marta'

let idade1 = 10
let idade2 = '10'

console.log('Igual')
console.log(nome1 === nome2)
console.log(idade1 == idade2)
console.log(idade1 === idade2)

console.log('\nDiferente')
console.log(nome1 != nome2)
console.log(idade1 !== idade2)

console.log('\nAND')
console.log(1 < 2 && 1 < 3)
console.log(10 < 2 && 1 < 3)

console.log('\nOR')
console.log(10 < 2 || 1 < 3)